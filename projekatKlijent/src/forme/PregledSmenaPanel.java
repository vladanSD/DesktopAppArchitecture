    public void sredi() {
        toZahtev.setOperacija(Konstante.VRATI_RADNESMENE);
        try {
            Komunikacija.getInstance().posalji(toZahtev);
            toOdgovor = Komunikacija.getInstance().procitaj();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
        if (!toOdgovor.getPoruka().equals("")) {
            JOptionPane.showMessageDialog(this, toOdgovor.getPoruka(), "Greška", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        List<RadneSmene> lista = (List<RadneSmene>)toOdgovor.getOdgovor();
        modelTabeleRadneSmene model = new modelTabeleRadneSmene(lista);
        jTable1.setModel(model);
    }
