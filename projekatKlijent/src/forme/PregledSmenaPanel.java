    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        RadneSmene smena = new RadneSmene();
        int vreme = 0;
        try {
            vreme = Integer.parseInt(jTextField1.getText().trim());
        } catch (Exception e) {
        }
        smena.setVremeRadneSmene(vreme);
        Zaposleni z = new Zaposleni();
        z.setImeIPrezime(jTextField1.getText().trim());
        smena.setZaposleni(z);
        
        toZahtev.setOperacija(Konstante.VRATI_SMENE_PO_KRITERIJUMU);
        toZahtev.setParametar(smena);
        try {
            Komunikacija.getInstance().posalji(toZahtev);
            toOdgovor = Komunikacija.getInstance().procitaj();
        } catch (IOException ex) {
            Logger.getLogger(PregledSmenaPanel.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(PregledSmenaPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
        if(!toOdgovor.getPoruka().equals("")){
            JOptionPane.showMessageDialog(this, toOdgovor.getPoruka(),"Greška", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        List<RadneSmene> lista = (List<RadneSmene>) toOdgovor.getOdgovor();
        if(lista.isEmpty()){
            modelTabeleRadneSmene model = new modelTabeleRadneSmene(lista);
            jTable1.setModel(model);
            JOptionPane.showMessageDialog(this, "Nije moguće pronaći radne smene po datom kriterijumu", "Neuspeh", JOptionPane.ERROR_MESSAGE);
            return;
        }
        modelTabeleRadneSmene model = new modelTabeleRadneSmene(lista);
            jTable1.setModel(model);
            JOptionPane.showMessageDialog(this, "Uspešno pronadjene smene!","Uspeh",JOptionPane.INFORMATION_MESSAGE);
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButtonIzmeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonIzmeniActionPerformed
        // TODO add your handling code here:
        modelTabeleRadneSmene model = (modelTabeleRadneSmene) jTable1.getModel();
        int red = jTable1.getSelectedRow();
        if(red==-1){
            JOptionPane.showMessageDialog(this, "Selektujte odredjenu smenu","Greška", JOptionPane.ERROR_MESSAGE);
            return;
        }
        RadneSmene smena = model.vratiSmenu(red);
        
        glavna.pozoviUpdateSmene(smena, glavna);
        
    }//GEN-LAST:event_jButtonIzmeniActionPerformed


