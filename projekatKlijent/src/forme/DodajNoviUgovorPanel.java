    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if(!validacija()) return;
        int ugovorID = Integer.parseInt(jTextFieldUgovorID.getText().trim());
       
        
        String nazivFirme = jTextFieldFirma.getText().trim();
        String pib = jTextFieldPIB.getText().trim();
        String opis = jTextFieldOpis.getText().trim();
        String napomene = jTextFieldNapomene.getText().trim();
        Usluge usluga = (Usluge) jComboBoxUsluge.getSelectedItem();
        Koordinator koordinator = Logovanje.getInstance().getUlogovaniKoordinator();
        
        modelTabeleStavkeUgovora model = (modelTabeleStavkeUgovora) jTable1.getModel();
        List<StavkeUgovora> lista = model.vratiListu();
        Ugovori ugo = new Ugovori(ugovorID, nazivFirme, pib, opis, napomene, usluga, koordinator);
        ugo.setListaStavki(lista);
        
        toZahtev.setOperacija(Konstante.SACUVAJ_UGOVOR);
        toZahtev.setParametar(ugo);
        
        try {
            Komunikacija.getInstance().posalji(toZahtev);
            toOdgovor = Komunikacija.getInstance().procitaj();
            if(!toOdgovor.getPoruka().equals("")){
                JOptionPane.showMessageDialog(this, toOdgovor.getPoruka());
                return;
            }
            JOptionPane.showMessageDialog(this, "Sistem je uspešno zapamtio podatke novog ugovora!");
            resetuj();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
            
        }
        
        
    }//GEN-LAST:event_jButton3ActionPerformed


