public void sredi() {
        toZahtev.setOperacija(Konstante.VRATI_USLUGE);
        try {
            Komunikacija.getInstance().posalji(toZahtev);
            toOdgovor = Komunikacija.getInstance().procitaj();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
        if (!toOdgovor.getPoruka().equals("")) {
            JOptionPane.showMessageDialog(this, toOdgovor.getPoruka(), "Greška", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        jComboBoxUsluge.removeAllItems();
        List<Usluge> lista = (List<Usluge>) toOdgovor.getOdgovor();
        for (Usluge u : lista) {
            jComboBoxUsluge.addItem(u);
        }
        
        modelTabeleStavkeUgovora model = new modelTabeleStavkeUgovora();
        jTable1.setModel(model);
}


    public void resetuj(){
        jTextFieldFirma.setText("");
        jTextFieldPIB.setText("");
        jTextFieldOpis.setText("");
        jTextFieldNapomene.setText("");
        jTextFieldUgovorID.setText("");
        jComboBoxUsluge.setSelectedIndex(0);
        modelTabeleStavkeUgovora model = new modelTabeleStavkeUgovora();
        jTable1.setModel(model);
        
    
    }
    
    private boolean validacija(){
        int ugovorID;
        String naziv = jTextFieldFirma.getText().trim();
        String pib = jTextFieldPIB.getText().trim();
        String opis = jTextFieldOpis.getText().trim();
        String napomene = jTextFieldNapomene.getText().trim();
        
        try {
            ugovorID = Integer.parseInt(jTextFieldUgovorID.getText().trim());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "ID ugovora mora biti broj!","Greška", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if(naziv.equals("") || pib.equals("") || opis.equals("") || napomene.equals("")){
            JOptionPane.showMessageDialog(this, "Popunite sva polja","Greška", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return true;
        
        
        
        
    
    }
