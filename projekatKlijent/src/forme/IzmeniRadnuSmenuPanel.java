    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        if(!validacija()) return;
        
        //ovde sam samo datum proveravao, ostalo je u validaciji
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
        Date datum;
        try {
            datum = sdf.parse(jTextFieldDatum.getText().trim());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Datum mora biti u obliku dd/MM/yyyy");
            return;
        }
        
        int vreme = Integer.parseInt(jTextFieldSmena.getText().trim());
        Zaposleni z = (Zaposleni) jComboBoxZaposleni.getSelectedItem();
        
        
        smena.setDatumRadneSmene(datum);
        smena.setZaposleni(z);
        smena.setVremeRadneSmene(vreme);
        smena.setKoordinatorID(Logovanje.getInstance().getUlogovaniKoordinator());
        
        
        toZahtev.setOperacija(Konstante.IZMENI_RADNUSMENU);
        toZahtev.setParametar(smena);
        try {
            Komunikacija.getInstance().posalji(toZahtev);
            toOdgovor = Komunikacija.getInstance().procitaj();
        } catch (IOException ex) {
            Logger.getLogger(IzmeniRadnuSmenuPanel.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(IzmeniRadnuSmenuPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (!toOdgovor.getPoruka().equals("")) {
            JOptionPane.showMessageDialog(this, toOdgovor.getPoruka(), "Greška", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        JOptionPane.showMessageDialog(this, "Sistem je uspešno sačuvao radnu smenu!","Info",JOptionPane.INFORMATION_MESSAGE);
        glavna.zameniAktivniPanel(AktivanPanel.PRETRAGA_RASPOREDA);
    }//GEN-LAST:event_jButton1ActionPerformed


