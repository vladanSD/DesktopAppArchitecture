    private void pocetno(){
       // jComboBoxZaposleni.setSelectedItem(smena.getZaposleni());
        SimpleDateFormat sdf  = new SimpleDateFormat("dd/MM/yyyy");
        String datum = sdf.format(smena.getDatumRadneSmene());
        jTextFieldDatum.setText(datum);
        jTextFieldSmena.setText(String.valueOf(smena.getVremeRadneSmene()));
    }
    
    private void srediKombo(){
        toZahtev.setOperacija(Konstante.VRATI_ZAPOSLENE);
        try {
            Komunikacija.getInstance().posalji(toZahtev);
            toOdgovor = Komunikacija.getInstance().procitaj();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
        if (!toOdgovor.getPoruka().equals("")) {
            JOptionPane.showMessageDialog(this, toOdgovor.getPoruka(), "Greška", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        List<Zaposleni> lista = (List<Zaposleni>) toOdgovor.getOdgovor();
        jComboBoxZaposleni.removeAllItems();
        for(Zaposleni z : lista){
                jComboBoxZaposleni.addItem(z);
                if(smena.getZaposleni().equals(z))
                    jComboBoxZaposleni.setSelectedItem(z);
            
        }
    }
    
    private boolean  validacija(){
        String smenaString = jTextFieldSmena.getText().trim();
        String datumString= jTextFieldDatum.getText().trim();
        if(smenaString.equals("") || datumString.equals("")) return false;
        int smenaInt;
        try {
            smenaInt = Integer.parseInt(smenaString);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Smena se unosi kao 1,2 ili 3", "Greška", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if(smenaInt<1 || smenaInt>3){
            JOptionPane.showMessageDialog(this, "Smena se unosi kao 1,2 ili 3", "Greška", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return true;
    }

