    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String naziv = jTextField1.getText().trim();
        String pib = jTextField1.getText().trim();
        int id= -1;
        try {
            id = Integer.parseInt(jTextField1.getText().trim());
        } catch (Exception e) {
        }
        Koordinator k = Logovanje.getInstance().getUlogovaniKoordinator();
        
        Ugovori u = new Ugovori();
        u.setNazivFirme(naziv);
        u.setPib(pib);
        u.setUgovorID(id);
        u.setKoordinator(k);
        
        toZahtev.setOperacija(Konstante.VRATI_UGOVORE_PO_KRITERIJUMU);
        toZahtev.setParametar(u);
        
        try {
            Komunikacija.getInstance().posalji(toZahtev);
            toOdgovor = Komunikacija.getInstance().procitaj();
        } catch (IOException ex) {
            Logger.getLogger(PretragaUgovoraPanel.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(PretragaUgovoraPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (!toOdgovor.getPoruka().equals("")) {
            JOptionPane.showMessageDialog(this, toOdgovor.getPoruka(), "Greška", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        List<Ugovori> lista =  (List<Ugovori>) toOdgovor.getOdgovor();
        if(lista.isEmpty()){
            modelTabeleUgovori model = new modelTabeleUgovori(lista);
        jTable1.setModel(model);
        JOptionPane.showMessageDialog(this, "Ne postoji ni jedan ugovor po zadatom kriterijumu","Greška", JOptionPane.ERROR_MESSAGE);
        return;
        }
        modelTabeleUgovori model = new modelTabeleUgovori(lista);
        jTable1.setModel(model);
        JOptionPane.showMessageDialog(this, "Uspešno pronadjeni ugovori!");
        
    }//GEN-LAST:event_jButton1ActionPerformed

