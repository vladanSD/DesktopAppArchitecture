    public void sredi() {
        Ugovori u = new Ugovori();
        Koordinator k = Logovanje.getInstance().getUlogovaniKoordinator();
        u.setKoordinator(k);
        toZahtev.setOperacija(Konstante.VRATI_UGOVORE_PO_KOORDINATORU);
        toZahtev.setParametar(u);
        
        try {
            Komunikacija.getInstance().posalji(toZahtev);
            toOdgovor = Komunikacija.getInstance().procitaj();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
        if (!toOdgovor.getPoruka().equals("")) {
            JOptionPane.showMessageDialog(this, toOdgovor.getPoruka(), "Greška", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        modelTabeleUgovori model1 = new modelTabeleUgovori((List<Ugovori>) toOdgovor.getOdgovor());
        jTable1.setModel(model1);
        
    
            
        }

